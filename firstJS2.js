// Different between float and double.

// Float: 
	// 1. Full name is floating-point.
	// 2. Size in memory is 4 bytes (32 bits).
	// 3. Precision is about 6-7 decimal digits.
	// 4. Range is approximately plus or minus 3.4 * 10^38.
	// 5. Faster in some systems.
	// 6. Use f or F (e.g., 3.14f).
	// 7. It is used when memory is limited and high precision isn't required.

// Double:
	// 1. Full name is double-precision floating-point.
	// 2. Size in memory is 8 bytes (64 bits).
	// 3. Precision is about 15-16 decimal digits.
	// 4. Range is approximately plus or minus 1.7 * 10^308.
	// 5. It is slightly slower because of more precision to handle.
	// 6. Default for decimals (e.g., 3.14).
	// 7. It is used when you need accurate decimal calculations.


